module mod_msg
  use lib_const
  use lib_log
  implicit none
  !-------------------------------------------------------------
  character(CLEN_VAR), parameter :: MODNAM = 'mod_msg'
  !-------------------------------------------------------------
contains
!===============================================================
!
!===============================================================
subroutine test_msg()
  implicit none

  call logbgn('test_msg', MODNAM)
  !-------------------------------------------------------------
  !
  !-------------------------------------------------------------
  call logmsg('Mode: not quite in error')
  call setlog('-q')
  !-------------------------------------------------------------
  !
  !-------------------------------------------------------------
  call logmsg('---- Test erradd ----------------------------------------------')
  call erradd('Error message 1')
  call errend()
  !-------------------------------------------------------------
  !
  !-------------------------------------------------------------
  call logmsg('---- Test errapd ----------------------------------------------')
  call erradd('Error message 2')
  call errapd('  Error message 2-1')
  call errapd('  Error message 2-2')
  call errend()
  !-------------------------------------------------------------
  !
  !-------------------------------------------------------------
  call logmsg('---- Test errret ----------------------------------------------')
  if( func1() /= 0 )then
    call errend('Error from func1')
  endif
  !-------------------------------------------------------------
  call logmsg('---------------------------------------------------------------')
  call logret()
end subroutine test_msg
!===============================================================
!
!===============================================================
integer function func1() result(res)
  implicit none

  call logbgn('func1', MODNAM)
  res = 0
  !-------------------------------------------------------------
  if( func1_1() /= 0 )then
    res = 1
    call errret('Error from func1_1')
    return
  endif
  !-------------------------------------------------------------
  call logret()
end function func1
!===============================================================
!
!===============================================================
integer function func1_1() result(res)
  implicit none

  call logbgn('func1_1', MODNAM)
  res = 0
  !-------------------------------------------------------------
  res = 1
  call errret('Error in func1_1')
  return
  !-------------------------------------------------------------
  call logret()
end function func1_1
!===============================================================
!
!===============================================================
integer function func1_2() result(res)
  implicit none

  call logbgn('func1_2', MODNAM)
  res = 0
  !-------------------------------------------------------------

  !-------------------------------------------------------------
  call logret()
end function func1_2
!===============================================================
!
!===============================================================
end module mod_msg
