subroutine test_time()
  implicit none

  call logbgn('test_time', MODNAM, '+tr')
  !-------------------------------------------------------------
  ! [Target] To test time measurement.
  ! [Results] Time is measured?
  !   sub1: True
  !     sub1_1: True
  !-------------------------------------------------------------
  call logmsg('Call subroutines with time measuring mode.')

  call sub1()
  !-------------------------------------------------------------
  ! [Target] To test switching time measurement off.
  ! [Results] Time is measured?
  !   sub2: True
  !     sub2_1: False
  !     sub2_2: True
  !   sub3: False
  !     sub3_1: True
  !     sub3_2: False
  !-------------------------------------------------------------
  call sub2()

  call sub3()
  !-------------------------------------------------------------
  call logret()
end subroutine test_time
